#!/usr/bin/env node
// =====================================================================
// FILE: tools/generate-cert-catalog.js
// Regenerates src/CertCatalog.php from assets/js/certs-catalog.js.
//
// Run whenever the JS cert catalog changes:
//   node tools/generate-cert-catalog.js
//
// Emits CERT_MAP (normalized cert names + aliases -> category ids),
// TOKEN_MAP (cert-domain token -> category ids), and categoriesForCert,
// which resolves: exact match -> longest-substring containment ->
// token vote -> EducationCatalog field fallback (cert names phrased
// like fields of study, e.g. "Certificate in Accounting").
// =====================================================================

const fs = require("fs");
const path = require("path");

const ROOT  = path.join(__dirname, "..");
const JOBS  = path.join(ROOT, "assets/js/jobs-catalog.js");
const CERTS = path.join(ROOT, "assets/js/certs-catalog.js");
const OUT   = path.join(ROOT, "src/CertCatalog.php");

// Category name -> index, from the job catalog (the authority).
const jobsJs = fs.readFileSync(JOBS, "utf8");
const jm = jobsJs.match(/const JOB_CATALOG = (\[[\s\S]*?\n\]);/);
if (!jm) { console.error("Could not find JOB_CATALOG in " + JOBS); process.exit(1); }
const JOB_CATALOG = eval(jm[1]);
const ci = {};
JOB_CATALOG.forEach((g, i) => { ci[g.category] = i; });

const certsJs = fs.readFileSync(CERTS, "utf8");
const cm = certsJs.match(/const CERT_CATALOG = (\[[\s\S]*?\n\]);/);
if (!cm) { console.error("Could not find CERT_CATALOG in " + CERTS); process.exit(1); }
const CERT_CATALOG = eval(cm[1]);
const tm = certsJs.match(/const CERT_TOKENS = (\{[\s\S]*?\n\});/);
if (!tm) { console.error("Could not find CERT_TOKENS in " + CERTS); process.exit(1); }
const CERT_TOKENS = eval("(" + tm[1] + ")");

const norm = s => s.toLowerCase().replace(/[–—]/g, "-").replace(/\s+/g, " ").trim();
const toIds = (cats, where) => cats.map(c => {
  if (!(c in ci)) { console.error(`Unknown job category "${c}" on ${where}`); process.exit(1); }
  return ci[c];
});

// CERT_MAP: canonical names + aliases, all normalized. When two entries
// share a key (e.g. family aliases), the union of categories wins.
const certMap = {};
const roster = [];   // structured list for the admin review screen
let names = 0, aliases = 0;
const put = (key, ids) => {
  if (!key) return;
  certMap[key] = certMap[key] ? [...new Set([...certMap[key], ...ids])] : ids;
};
for (const g of CERT_CATALOG) {
  for (const c of g.certs) {
    const ids = toIds(c.cats || [], `cert "${c.name}"`);
    if (!ids.length) { console.error(`No categories on cert "${c.name}"`); process.exit(1); }
    put(norm(c.name), ids); names++;
    for (const a of (c.aliases || [])) { put(norm(a), ids); aliases++; }
    roster.push({ name: c.name, issuer: c.issuer || "", group: g.group,
                  cats: ids, aliases: (c.aliases || []) });
  }
}

const tokenMap = {};
for (const [tok, cats] of Object.entries(CERT_TOKENS)) {
  tokenMap[norm(tok)] = toIds(cats, `token "${tok}"`);
}

const phpStr = s => "'" + s.replace(/\\/g, "\\\\").replace(/'/g, "\\'") + "'";
const phpIds = ids => "[" + ids.join(",") + "]";

let out = `<?php

require_once __DIR__ . '/EducationCatalog.php';

// =====================================================================
// FILE: src/CertCatalog.php
// ---------------------------------------------------------------------
// GENERATED by tools/generate-cert-catalog.js from
// assets/js/certs-catalog.js — DO NOT hand-edit the data arrays.
// Rerun the generator after every catalog change.
//
// Maps certification names/issuers to JobCatalog categories, mirroring
// the EducationCatalog pattern. Certifications speak a vocabulary of
// vendors, acronyms, and program names ("CCNA", "PMP", "ServSafe") that
// barely overlaps job-title tokens, so ScoreEngine resolves a cert here
// first (full credit on a direct category hit, half on adjacent) and
// only falls back to fuzzy matching when the catalog doesn't know it.
//
// Resolution order in categoriesForCert:
//   1. Exact CERT_MAP match on normalized "name issuer".
//   2. Longest-substring CERT_MAP containment (so "american welding
//      society" wins before the "AWS" cloud family could misfire).
//   3. TOKEN_MAP vote — cert-domain vocabulary for the uncataloged
//      long tail ("Fortinet NSE 4 Network Security" hits 'fortinet',
//      'network', 'security').
//   4. EducationCatalog field fallback — certs phrased like fields of
//      study ("Certificate in Accounting", "Human Resource Management
//      Certificate").
// =====================================================================

class CertCatalog
{
    public const CERT_MAP = [
`;
const keysSorted = Object.keys(certMap).sort();
for (const k of keysSorted) out += `        ${phpStr(k)} => ${phpIds(certMap[k])},\n`;
out += `    ];

    public const ROSTER = [
`;
for (const r of roster.slice().sort((a, b) => a.name.localeCompare(b.name))) {
  out += `        ['name' => ${phpStr(r.name)}, 'issuer' => ${phpStr(r.issuer)}, 'group' => ${phpStr(r.group)}, `
       + `'cats' => ${phpIds(r.cats)}, 'aliases' => [${r.aliases.map(phpStr).join(",")}]],\n`;
}
out += `    ];

    public const TOKEN_MAP = [
`;
for (const k of Object.keys(tokenMap).sort()) out += `        ${phpStr(k)} => ${phpIds(tokenMap[k])},\n`;
out += `    ];

    // Admin-added catalog entries (cert_catalog_entries), merged with the
    // static CERT_MAP at resolution time. Custom keys win collisions so
    // admins can also override a static mapping. Loaded once per request
    // by ScoreEngine::gatherProfile; when never loaded (or the table is
    // absent) resolution is static-only, so scoring never breaks on a
    // missing migration.
    private static ?array $customMap = null;
    private static ?array $keyCache  = null;   // sorted union of match keys

    public static function loadCustom(PDO $pdo): void
    {
        if (self::$customMap !== null) return;   // per-request cache
        self::$customMap = [];
        try {
            $rows = $pdo->query('SELECT name, issuer, aliases, cats FROM cert_catalog_entries')->fetchAll();
        } catch (Throwable $e) {
            return;   // table not migrated yet — static catalog only
        }
        foreach ($rows as $r) {
            $ids = array_values(array_filter(array_map('intval', (array) json_decode($r['cats'] ?? '[]', true)),
                fn($i) => $i >= 0));
            if (!$ids) continue;
            $keys = [self::normText($r['name'] . ' ' . ($r['issuer'] ?? '')), self::normText($r['name'])];
            foreach ((array) json_decode($r['aliases'] ?? '[]', true) as $a) {
                if (is_string($a) && trim($a) !== '') $keys[] = self::normText($a);
            }
            foreach ($keys as $k) if ($k !== '') self::$customMap[$k] = $ids;
        }
        self::$keyCache = null;   // rebuild the sorted union on next resolve
    }

    /** Test hook: reset the per-request custom cache. */
    public static function resetCustom(): void { self::$customMap = null; self::$keyCache = null; }

    private static function normText(string $s): string
    {
        $s = strtolower(trim($s));
        $s = str_replace(["\\u{2013}", "\\u{2014}"], '-', $s);
        return preg_replace('/\\s+/', ' ', $s);
    }

    /**
     * The built-in catalog as a structured roster (canonical name,
     * issuer, group, category ids, aliases) — powers the admin review
     * screen. The flat CERT_MAP above is what resolution actually uses;
     * this is the human-readable view of the same source data. Built-in
     * entries live in code, so admins adjust one by saving an override
     * (a cert_catalog_entries row keyed on the same name), which wins
     * during resolution.
     */
    public static function staticRoster(): array
    {
        return self::ROSTER;
    }

    /**
     * Resolve a certification (name + issuer) to job categories, or
     * null when nothing in the catalog (static or admin-added), token
     * vocabulary, or education field map recognizes it.
     */
    public static function categoriesForCert(string $name, string $issuer = ''): ?array
    {
        $norm = self::normText($name . ' ' . $issuer);
        if ($norm === '') return null;

        $custom = self::$customMap ?? [];
        if (isset($custom[$norm]))        return $custom[$norm];
        if (isset(self::CERT_MAP[$norm])) return self::CERT_MAP[$norm];

        // Longest-substring containment over the UNION of custom and
        // static keys (custom wins ties via the lookup order above and
        // key precedence here).
        if (self::$keyCache === null) {
            self::$keyCache = array_merge(array_keys($custom), array_keys(self::CERT_MAP));
            usort(self::$keyCache, fn($a, $b) => strlen($b) <=> strlen($a));
        }
        foreach (self::$keyCache as $key) {
            if (str_contains($norm, $key)) return $custom[$key] ?? self::CERT_MAP[$key];
        }

        // Token vote: collect every category any known token points at.
        $votes = [];
        foreach (preg_split('/[^a-z0-9+&]+/', $norm) as $tok) {
            if ($tok === '' || !isset(self::TOKEN_MAP[$tok])) continue;
            foreach (self::TOKEN_MAP[$tok] as $cid) $votes[$cid] = true;
        }
        if ($votes) return array_keys($votes);

        // Field-of-study fallback: many certificates are named after the
        // field ("Certificate in Accounting").
        return EducationCatalog::categoriesForField($norm);
    }
}
`;
fs.writeFileSync(OUT, out);
console.log(`Wrote ${OUT}: ${names} certs, ${aliases} aliases, ${Object.keys(certMap).length} map keys, ${Object.keys(tokenMap).length} tokens`);
