#!/usr/bin/env node
// =====================================================================
// FILE: tools/generate-education-catalog.js
// Regenerates src/EducationCatalog.php from assets/js/education-catalog.js.
//
// Run whenever the education catalog changes:
//   node tools/generate-education-catalog.js
//
// Category names in each field's `cats` are resolved to category IDS
// against assets/js/jobs-catalog.js (same ids as src/JobCatalog.php),
// so run tools/generate-job-catalog.js FIRST if the job catalog also
// changed. Any unknown category name fails the build loudly.
// =====================================================================

const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const JOBS = path.join(ROOT, "assets/js/jobs-catalog.js");
const EDU  = path.join(ROOT, "assets/js/education-catalog.js");
const OUT  = path.join(ROOT, "src/EducationCatalog.php");

const jobsJs = fs.readFileSync(JOBS, "utf8");
const jm = jobsJs.match(/const JOB_CATALOG = (\[[\s\S]*?\n\]);/);
if (!jm) { console.error("Could not find JOB_CATALOG in " + JOBS); process.exit(1); }
const JOB_CATALOG = eval(jm[1]);
const ci = Object.fromEntries(JOB_CATALOG.map((c, i) => [c.category, i]));

const eduJs = fs.readFileSync(EDU, "utf8");
const em = eduJs.match(/const EDU_CATALOG = (\[[\s\S]*?\n\]);/);
if (!em) { console.error("Could not find EDU_CATALOG in " + EDU); process.exit(1); }
const EDU_CATALOG = eval(em[1]);

const fieldMap = {};
let count = 0;
for (const g of EDU_CATALOG) {
  for (const f of g.fields) {
    const ids = f.cats.map(c => {
      if (!(c in ci)) { console.error(`Unknown job category "${c}" on field "${f.name}"`); process.exit(1); }
      return ci[c];
    });
    fieldMap[f.name.toLowerCase()] = ids;
    count++;
  }
}

const phpStr = s => "'" + s.replace(/\\/g, "\\\\").replace(/'/g, "\\'") + "'";

let out = "<?php\n";
out += `
// =====================================================================
// FILE: src/EducationCatalog.php
// ---------------------------------------------------------------------
// GENERATED by tools/generate-education-catalog.js from
// assets/js/education-catalog.js — DO NOT hand-edit the data array.
// Regenerate after any catalog change:
//   node tools/generate-education-catalog.js
//
// Maps normalized fields of study to JobCatalog category ids, giving
// ScoreEngine deterministic education relevance when a profile's field
// matches (or contains) a catalog entry. Fields with an empty id list
// are recognized but carry no category signal (e.g. General Studies).
// =====================================================================

class EducationCatalog
{
`;
out += "    public const FIELD_MAP = [\n";
for (const k of Object.keys(fieldMap).sort()) {
  out += `        ${phpStr(k)} => [${fieldMap[k].join(",")}],\n`;
}
out += "    ];\n";
out += `
    /**
     * Category ids for a field-of-study string, or null when the text
     * doesn't correspond to any catalog field. Tries an exact match on
     * the normalized text first, then containment (so "BS in Computer
     * Science" still resolves). Longest catalog names are checked first
     * so "Computer Engineering" wins over "Engineering"-style overlaps.
     */
    public static function categoriesForField(string $text): ?array
    {
        $norm = strtolower(trim($text));
        if ($norm === '') return null;
        if (isset(self::FIELD_MAP[$norm])) return self::FIELD_MAP[$norm];

        static $byLength = null;
        if ($byLength === null) {
            $byLength = array_keys(self::FIELD_MAP);
            usort($byLength, fn($a, $b) => strlen($b) <=> strlen($a));
        }
        foreach ($byLength as $name) {
            if (str_contains($norm, $name)) return self::FIELD_MAP[$name];
        }
        return null;
    }
}
`;

fs.writeFileSync(OUT, out);
console.log(`Wrote ${OUT}: ${count} fields`);
